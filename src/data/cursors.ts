const cursorOptionById = new Map(KORE_CURSOR_OPTIONS.map((cursor) => [cursor.id, cursor]));

export function getKoreCursorOption(id: string): KoreCursorOption {
  return cursorOptionById.get(id) ?? cursorOptionById.get(KORE_DEFAULT_CURSOR_ID) ?? KORE_CURSOR_OPTIONS[0]!;
}

export function isKoreCursorId(id: unknown): id is string {
  return typeof id === 'string' && cursorOptionById.has(id);
}
